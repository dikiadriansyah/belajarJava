public class SomeObject {
    private String data; // Gantilah dengan tipe data yang sesuai kebutuhan

    // Konstruktor untuk membuat objek SomeObject
    public SomeObject() {
        // Inisialisasi objek dengan nilai default atau sesuai kebutuhan
        this.data = "Default Data";
    }

    // Metode setter untuk mengatur data objek SomeObject
    public void setData(String data) {
        this.data = data;
    }

    // Metode getter untuk mendapatkan data objek SomeObject
    public String getData() {
        return data;
    }
}
