public class The_idea_behind_annotations {

    // The idea behind Annotations / Ide di balik Anotasi

    // Spesifikasi Bahasa Java menjelaskan Anotasi sebagai berikut:
    /*
     * Anotasi adalah penanda yang mengaitkan informasi dengan konstruksi program,
     * namun tidak berpengaruh pada waktu berjalan.
     */

    /*
     * Anotasi mungkin muncul sebelum tipe atau deklarasi. Dimungkinkan bagi mereka
     * untuk muncul di tempat di mana mereka dapat mengajukan permohonan baik untuk
     * suatu tipe maupun deklarasi.
     * Apa sebenarnya penerapan anotasi diatur oleh "meta-annotation" @Target. Lihat
     * "Mendefinisikan jenis anotasi" untuk informasi lebih lanjut.
     * 
     * Anotasi digunakan untuk banyak tujuan. Kerangka kerja seperti Spring dan
     * Spring-MVC menggunakan anotasi untuk menentukan di mana Dependensi harus
     * dimasukkan atau di mana permintaan harus dirutekan.
     * 
     * Kerangka kerja lain menggunakan anotasi untuk pembuatan kode. Lombok dan JPA
     * adalah contoh utama yang menggunakan anotasi untuk menghasilkan kode Java
     * (dan SQL).
     * 
     */

    /*
     * Topik ini bertujuan untuk memberikan gambaran komprehensif tentang:
     * Bagaimana cara mendefinisikan Anotasi Anda sendiri?
     * Anotasi apa yang disediakan oleh Bahasa Java?
     * Bagaimana Anotasi digunakan dalam praktik?
     */

    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}
