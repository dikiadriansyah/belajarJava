public class boolean_literals {
    // Boolean literals/ Boolean literal

    // Boolean literal adalah literal paling sederhana dalam bahasa pemrograman
    // Java. Dua kemungkinan nilai boolean diwakili oleh literal true dan false.
    // Ini peka terhadap huruf besar-kecil. Sebagai contoh:
    public static void main(String[] args) {
        boolean bendera = true;
        bendera = false;
        System.out.println(bendera);
        // hasil:
        // false

        // penjelasan:
        // awalnya membuat public class boolean_literals{} yg berisi public
        // static void main(String[] args){},
        // selanjutnya membuat <tipeData> namaVariabel yg berisi nilai true,
        // selanjutnya membuat lagi namaVariabel berisi nilai false,
        // selanjutnya tampilkan menggunakan System.out.println();
        // sehingga hasil false, karena awalnya nilai true ditimpa false sehingga yang
        // diambil nilai paling akhir
    }

}
